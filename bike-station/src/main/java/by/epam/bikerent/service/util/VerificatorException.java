package by.epam.bikerent.service.util;

public class VerificatorException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -732972250853515933L;

	public VerificatorException() {
		super();
	}

	public VerificatorException(String message, Throwable cause) {
		super(message, cause);
	}

	public VerificatorException(String message) {
		super(message);
	}

	public VerificatorException(Throwable cause) {
		super(cause);
	}
	

}
